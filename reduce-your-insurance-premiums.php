                <h3 class="page-header">How to Reduce Your Insurance Premiums</h3>
<p>The cost of your business insurance is largely determined by your business’ risk factors.</p>
<p>Many of the risks that could edge your business into a higher premium bracket can be minimised, reassuring your insurance company about your reduced level of risk.</p>
<p>As work environments become safer, the number of workers’ compensation claims continues to decline.  By performing regular safety checks, reinforcement of safe working behaviour of employees, eliminating hazards that cause injuries can reduce workers compensation premiums.</p>  
<p>Many insurance policies require you to take active steps to reduce risk. It’s important both to understand and implement these requirements. For example, a policy may specify certain security and safety measures that must be in place, such as keeping your alarm and sprinkler systems maintained or keeping codes secure. Failing to comply with these conditions could invalidate your insurance.</p>
<p>Assess your risk regularly and demonstrate to your insurance company that you are making an effort to prevent problems before they happen. Insurance companies want to know that you are aware of and trying to reduce your risk.</p>
<p>Go armed with a risk assessment and speak to your Insurance broker about getting the right risks covered. Your broker might learn something about your business they didn’t know about!</p>

<p style="font-weight: bold;">Before signing up to any type of insurance policy, you need to evaluate its suitability and value in terms of your individual circumstances. Think about:</p>
<ul>
	<li>What is and what is not covered</li>
	<li>Do you understand the term co-insurance?</li>
	<li>When does your protection commence?</li>
	<li>Conditions of insurance</li>
	<li>Check the claims procedures</li>
	<li>Do you understand the terms of a replacement policy?</li>
	<li>Renewal conditions</li>
	<li>Reputation</li>
	<li>Compare &amp; negotiate policies &amp; pricing</li>
</ul>
	
