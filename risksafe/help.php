<p><b>How does RiskSafe work?</b></p>
<p>Great question! RiskSafeis a platform for tracking work and paying 1099 workers. Work can be entered directly through the app and user interface (UI) or via an integration with your company's other sources of work data.</p> 
<p>Work Types are the large tiles across the top that make up your company's incentive structure or ways to get paid. These can be things like Hours, Activities, Expenses, or anything else that you might get paid for. They can be unit-based, time-based, mileage-based, and monetary-based.</p> 
<p>Any way you pay workers or get paid can be accommodated. Need custom Work Types? Contact us about it!</p> 

<p><b>How do I signup and conduct a risk assessment?</b></p>
<p>Simply click on ‘Try RiskSafe’, and in your general information when prompted and select the correct risk assessment that’s relevant to you (e.g. business risk assessment, heath &amp; safety risk assessment, etc.)</p>

<p><b>Why do I need to make a payment?</b></p>
<p>We are providing risk management tools that generally cost thousands of dollars for large organisations through licensing and software setup etc.  We’re different in a way that we provide Saas solutions to you so that small to medium businesses can use the same type of software as large organisations (we believe SMEs need risk management the most).  To keep our business running and to keep improving our product and customer service, we need to charge a small fee.  We hope you understand.</p>  

<p><b>Can I export the files and risk assessments?</b></p>
<p>Yes once you complete the risk assessment by using the drop down boxes of types of risks and assessing your own circumstances, you are able to export to CSV, Word or Excel files.</p>  

<p><b>How do I conduct an audit of my controls?</b></p>
<p>We’ve made it self explanatory for you to conduct an audit of your controls.  You would have identified your key controls during the risk assessment phase (steps you have in place to prevent the risk from occurring).  You simply select the control you want to run an audit on, include the expected outcomes if the control is to run perfectly, test against these expectations and include your responses to each.  At the end, determine if your control is effective or if it needs further treatment.</p>  

<p><b>Can I speak to an expert if I need help?</b></p>
<p>Yes absolutely, RiskSafe was built by Risk Managers and Accountants from Large Global Banks and Big for Consulting firms who realised Risk management is needed most by small to medium businesses.  Drop us a line or give us a call and we’d love to go through your business risks with you.</p>  

<p><b>Can more than one person use RiskSafe in my company?</b></p>
<p>At this stage we accounts can only be created per user.   As we continue to build on RiskSafe, we plan on rolling out an enterprise solution where many users can log in under one account in the near future.</p>

